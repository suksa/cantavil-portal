// ── AI 없이 키워드로 질문을 매칭하는 함수 ───────────────────────────────
function norm(s: string): string {
  return s
    .toLowerCase()
    .replace(/\s+/g, '')
    .replace(/[^\p{L}\p{N}]/gu, '');
}

export interface FaqMatch {
  entry: FaqEntry;
  score: number;
}

/**
 * 입력 문장을 정규화한 뒤, 각 항목의 keywords/질문과 겹치는 정도로 점수를 매겨
 * 가장 잘 맞는 항목들을 점수 높은 순으로 돌려줍니다. (AI·외부호출 없음)
 */
export function matchFaq(input: string, limit = 3): FaqMatch[] {
  const q = norm(input);
  if (q.length < 1) return [];

  const matches: FaqMatch[] = [];
  for (const entry of FAQ) {
    let score = 0;
    for (const kw of entry.keywords) {
      const k = norm(kw);
      if (!k) continue;
      if (q.includes(k)) {
        // 긴 키워드가 더 구체적이라 가중치를 높게.
        score += Math.min(6, k.length + 1);
      } else if (q.length >= 2 && k.includes(q)) {
        score += 1;
      }
    }
    // 질문 문장과 직접 겹쳐도 가산점.
    const nq = norm(entry.q);
    if (q.length >= 2 && nq.includes(q)) score += 2;

    if (score > 0) matches.push({ entry, score });
  }

  matches.sort((a, b) => b.score - a.score);
  return matches.slice(0, limit);
}

export function faqById(id: string): FaqEntry | undefined {
  return FAQ.find((e) => e.id === id);
}

export function faqByCategory(cat: FaqCategoryId): FaqEntry[] {
  return FAQ.filter((e) => e.cat === cat);
}
